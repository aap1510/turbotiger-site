create or replace function public.sobolao_participar_rpc(
  p_cod_bolao bigint,
  p_qtd_cotas integer,
  p_palpite_numeros jsonb default '[]'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path to 'mod_sobolao', 'public', 'extensions', 'auth', 'pg_temp'
as $$
declare
  v_uid uuid := auth.uid();
  v_id_usuario bigint;
  v_bolao mod_sobolao.tbl_sbl_boloes%rowtype;
  v_ocupadas integer;
  v_taxa_cota integer;
  v_valor_aposta bigint;
  v_taxa bigint;
  v_total bigint;
  v_participacao bigint;
begin
  if v_uid is null then
    return jsonb_build_object('ok', false, 'error', 'nao_autenticado');
  end if;
  if coalesce(p_qtd_cotas, 0) <= 0 then
    return jsonb_build_object('ok', false, 'error', 'dados_obrigatorios');
  end if;

  v_id_usuario := mod_sobolao.fn_sbl_usuario_atual();
  if v_id_usuario is null then
    return jsonb_build_object('ok', false, 'error', 'usuario_nao_encontrado');
  end if;

  select *
    into v_bolao
  from mod_sobolao.tbl_sbl_boloes
  where cod_bolao = p_cod_bolao
  for update;

  if not found or v_bolao.status not in ('aberto','fechando') or v_bolao.data_limite < now() then
    return jsonb_build_object('ok', false, 'error', 'bolao_indisponivel');
  end if;

  select coalesce(sum(qtd_cotas), 0)::integer
    into v_ocupadas
  from mod_sobolao.tbl_sbl_participacoes
  where id_bolao = v_bolao.cod_bolao
    and status in ('reservado','pago','confirmado','premiado');

  if v_ocupadas + p_qtd_cotas > v_bolao.qtd_cotas then
    return jsonb_build_object('ok', false, 'error', 'cotas_insuficientes', 'cotas_disponiveis', greatest(v_bolao.qtd_cotas - v_ocupadas, 0));
  end if;

  v_taxa_cota := round(v_bolao.valor_aposta_cota_centavos * v_bolao.taxa_gestao_percentual / 100.0)::integer;
  v_valor_aposta := v_bolao.valor_aposta_cota_centavos::bigint * p_qtd_cotas;
  v_taxa := v_taxa_cota::bigint * p_qtd_cotas;
  v_total := v_valor_aposta + v_taxa;

  insert into mod_sobolao.tbl_sbl_participacoes (
    id_bolao,
    id_usuario,
    qtd_cotas,
    valor_aposta_centavos,
    taxa_gestao_centavos,
    valor_total_centavos,
    status,
    palpite_numeros_json
  )
  values (
    v_bolao.cod_bolao,
    v_id_usuario,
    p_qtd_cotas,
    v_valor_aposta,
    v_taxa,
    v_total,
    'reservado',
    coalesce(p_palpite_numeros, '[]'::jsonb)
  )
  returning cod_participacao into v_participacao;

  insert into mod_sobolao.tbl_sbl_auditoria(uid_usuario, area, acao, alvo_tipo, alvo_id, detalhes_json)
  values (v_uid, 'sobolao', 'participar', 'participacao', v_participacao::text, jsonb_build_object('id_bolao', v_bolao.cod_bolao, 'qtd_cotas', p_qtd_cotas, 'valor_total_centavos', v_total));

  return jsonb_build_object(
    'ok', true,
    'participacao', jsonb_build_object(
      'cod_participacao', v_participacao,
      'id_bolao', v_bolao.cod_bolao,
      'qtd_cotas', p_qtd_cotas,
      'valor_total_centavos', v_total,
      'status', 'reservado'
    )
  );
end;
$$;

create or replace function public.adm_sobolao_loterica_salvar_rpc(
