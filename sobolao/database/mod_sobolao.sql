-- So Bolao - modulo acoplado Turbo Tiger
-- Aplicacao manual/local: tools/supabase-cli/supabase.exe db query --linked --file "turbotiger-site/sobolao/database/mod_sobolao.sql"

create schema if not exists mod_sobolao;
comment on schema mod_sobolao is 'Modulo So Bolao: organizacao operacional de boloes lotericos do ecossistema Turbo Tiger.';

create table if not exists mod_sobolao.tbl_sbl_modalidades (
  cod_modalidade bigint generated by default as identity primary key,
  chave text not null unique,
  nome text not null,
  qtd_numeros integer not null,
  minimo_numero integer not null,
  maximo_numero integer not null,
  ativo boolean not null default true,
  regras_json jsonb not null default '{}'::jsonb,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  constraint ck_sbl_modalidades_chave check (btrim(chave) <> ''),
  constraint ck_sbl_modalidades_intervalo check (minimo_numero > 0 and maximo_numero >= minimo_numero and qtd_numeros > 0)
);

create table if not exists mod_sobolao.tbl_sbl_lotericas (
  cod_loterica bigint generated by default as identity primary key,
  nome text not null,
  cnpj text not null unique,
  cidade text,
  uf text,
  endereco text,
  whatsapp text,
  email text,
  status text not null default 'analise',
  contrato_url text,
  dados_json jsonb not null default '{}'::jsonb,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  constraint ck_sbl_lotericas_cnpj check (cnpj ~ '^[0-9]{14}$'),
  constraint ck_sbl_lotericas_status check (status in ('analise','ativa','pausada','bloqueada'))
);

create table if not exists mod_sobolao.tbl_sbl_loterica_usuarios (
  cod_loterica_usuario bigint generated by default as identity primary key,
  id_loterica bigint not null references mod_sobolao.tbl_sbl_lotericas(cod_loterica) on delete cascade,
  uid_usuario uuid not null references auth.users(id) on delete cascade,
  email text,
  nome text,
  ativo boolean not null default true,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  unique (id_loterica, uid_usuario)
);

create table if not exists mod_sobolao.tbl_sbl_concursos (
  cod_concurso bigint generated by default as identity primary key,
  id_modalidade bigint not null references mod_sobolao.tbl_sbl_modalidades(cod_modalidade),
  numero integer not null,
  data_sorteio date not null,
  status text not null default 'programado',
  resultado_json jsonb not null default '[]'::jsonb,
  premio_estimado_centavos bigint not null default 0,
  apurado_em timestamptz,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  unique (id_modalidade, numero),
  constraint ck_sbl_concursos_numero check (numero > 0),
  constraint ck_sbl_concursos_status check (status in ('programado','registrado','apurado','cancelado'))
);

create table if not exists mod_sobolao.tbl_sbl_boloes (
  cod_bolao bigint generated by default as identity primary key,
  codigo_publico text not null unique default ('sbl_' || substr(replace((extensions.gen_random_uuid())::text, '-', ''), 1, 12)),
  id_concurso bigint not null references mod_sobolao.tbl_sbl_concursos(cod_concurso),
  id_loterica bigint references mod_sobolao.tbl_sbl_lotericas(cod_loterica),
  titulo text not null,
  status text not null default 'rascunho',
  qtd_cotas integer not null,
  valor_aposta_cota_centavos integer not null,
  taxa_gestao_percentual numeric(7,3) not null default 10,
  data_limite timestamptz not null,
  numeros_json jsonb not null default '[]'::jsonb,
  comprovante_url text,
  regras_json jsonb not null default '{}'::jsonb,
  observacao text,
  criado_por uuid references auth.users(id) on delete set null,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  constraint ck_sbl_boloes_status check (status in ('rascunho','aberto','fechando','registrado','apurado','encerrado','cancelado')),
  constraint ck_sbl_boloes_cotas check (qtd_cotas > 0),
  constraint ck_sbl_boloes_valor check (valor_aposta_cota_centavos > 0 and taxa_gestao_percentual >= 0)
);

create table if not exists mod_sobolao.tbl_sbl_participacoes (
  cod_participacao bigint generated by default as identity primary key,
  codigo_participacao text not null unique default ('sblp_' || substr(replace((extensions.gen_random_uuid())::text, '-', ''), 1, 14)),
  id_bolao bigint not null references mod_sobolao.tbl_sbl_boloes(cod_bolao) on delete cascade,
  id_usuario bigint not null references public.tbl_usuarios(cod_usuario) on delete cascade,
  qtd_cotas integer not null,
  valor_aposta_centavos bigint not null,
  taxa_gestao_centavos bigint not null,
  valor_total_centavos bigint not null,
  status text not null default 'reservado',
  metodo_pagamento text,
  pagamento_ref text,
  palpite_numeros_json jsonb not null default '[]'::jsonb,
  documento_url text,
  reservado_em timestamptz not null default now(),
  pago_em timestamptz,
  cancelado_em timestamptz,
  atualizado_em timestamptz not null default now(),
  constraint ck_sbl_participacoes_cotas check (qtd_cotas > 0),
  constraint ck_sbl_participacoes_valores check (valor_aposta_centavos >= 0 and taxa_gestao_centavos >= 0 and valor_total_centavos = valor_aposta_centavos + taxa_gestao_centavos),
  constraint ck_sbl_participacoes_status check (status in ('reservado','pago','confirmado','cancelado','estornado','premiado','encerrado'))
);

create table if not exists mod_sobolao.tbl_sbl_premios (
  cod_premio bigint generated by default as identity primary key,
  id_bolao bigint not null references mod_sobolao.tbl_sbl_boloes(cod_bolao) on delete cascade,
  faixa text not null,
  acertos integer,
  valor_total_centavos bigint not null default 0,
  status text not null default 'apurando',
  documento_url text,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  constraint ck_sbl_premios_status check (status in ('apurando','confirmado','pago','cancelado'))
);

create table if not exists mod_sobolao.tbl_sbl_rateios (
  cod_rateio bigint generated by default as identity primary key,
  id_premio bigint not null references mod_sobolao.tbl_sbl_premios(cod_premio) on delete cascade,
  id_participacao bigint not null references mod_sobolao.tbl_sbl_participacoes(cod_participacao) on delete cascade,
  id_usuario bigint not null references public.tbl_usuarios(cod_usuario) on delete cascade,
