create table if not exists mod_sobolao.tbl_sbl_documentos (
  cod_documento bigint generated by default as identity primary key,
  alvo_tipo text not null,
  alvo_id bigint not null,
  tipo text not null,
  url text not null,
  hash_sha256 text,
  observacao text,
  criado_por uuid references auth.users(id) on delete set null,
  criado_em timestamptz not null default now(),
  constraint ck_sbl_documentos_alvo check (alvo_tipo in ('bolao','participacao','loterica','premio','rateio')),
  constraint ck_sbl_documentos_tipo check (btrim(tipo) <> '' and btrim(url) <> '')
