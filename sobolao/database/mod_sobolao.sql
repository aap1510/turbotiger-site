create or replace function mod_sobolao.fn_sbl_usuario_atual()
returns bigint
language plpgsql
stable
security definer
set search_path to 'mod_sobolao', 'public', 'extensions', 'auth', 'pg_temp'
as $$
declare
  v_uid uuid := auth.uid();
  v_id_usuario bigint;
begin
  if v_uid is null then
    return null;
  end if;

  select u.cod_usuario
    into v_id_usuario
  from public.tbl_usuarios u
  where u.uid_usuario = v_uid
  limit 1;

  return v_id_usuario;
end;
$$;

create or replace function mod_sobolao.fn_sbl_admin_ok(p_acao text default 'acessar')
returns boolean
language plpgsql
stable
security definer
set search_path to 'mod_sobolao', 'mod_admin', 'public', 'extensions', 'auth', 'pg_temp'
as $$
declare
  v_uid uuid := auth.uid();
  v_ok boolean := false;
begin
  if v_uid is null then
    return false;
  end if;

  select coalesce((select p.ok from mod_admin.fn_adm_usuario_tem_permissao(v_uid, 'sobolao', coalesce(p_acao, 'acessar')) p limit 1), false)
      or coalesce((select p.ok from mod_admin.fn_adm_usuario_tem_permissao(v_uid, 'admin', 'acessar') p limit 1), false)
    into v_ok;

  return coalesce(v_ok, false);
end;
$$;

create or replace function public.sobolao_usuario_painel_rpc()
returns jsonb
language plpgsql
security definer
set search_path to 'mod_sobolao', 'public', 'extensions', 'auth', 'pg_temp'
as $$
declare
  v_uid uuid := auth.uid();
  v_id_usuario bigint;
  v_resumo jsonb;
  v_boloes jsonb;
  v_minhas jsonb;
  v_resultados jsonb;
  v_premios jsonb;
  v_chamados jsonb;
begin
  if v_uid is null then
    return jsonb_build_object('ok', false, 'error', 'nao_autenticado');
  end if;

  v_id_usuario := mod_sobolao.fn_sbl_usuario_atual();
  if v_id_usuario is null then
    return jsonb_build_object('ok', false, 'error', 'usuario_nao_encontrado');
  end if;

  select jsonb_build_object(
    'boloes_ativos', (select count(*) from mod_sobolao.tbl_sbl_boloes b where b.status in ('aberto','fechando','registrado')),
    'cotas_reservadas', coalesce((select sum(p.qtd_cotas) from mod_sobolao.tbl_sbl_participacoes p where p.id_usuario = v_id_usuario and p.status in ('reservado','pago','confirmado','premiado')), 0),
    'premios_centavos', coalesce((select sum(r.valor_centavos) from mod_sobolao.tbl_sbl_rateios r where r.id_usuario = v_id_usuario and r.status in ('confirmado','pago')), 0),
    'gestao_centavos', coalesce((select sum(p.taxa_gestao_centavos) from mod_sobolao.tbl_sbl_participacoes p where p.id_usuario = v_id_usuario and p.status in ('reservado','pago','confirmado','premiado')), 0),
    'parceiras', (select count(*) from mod_sobolao.tbl_sbl_lotericas l where l.status = 'ativa'),
    'pendencias', coalesce((select count(*) from mod_sobolao.tbl_sbl_participacoes p where p.id_usuario = v_id_usuario and p.status = 'reservado'), 0),
    'taxa_gestao_percentual', 10,
    'conversao_alvo_percentual', 30,
    'apostas_media_mes', 4,
    'ticket_oficial_centavos', 600
  ) into v_resumo;

  select coalesce(jsonb_agg(to_jsonb(s) order by s.data_limite), '[]'::jsonb)
    into v_boloes
  from (
    select
      b.cod_bolao,
      b.titulo,
      m.nome as modalidade,
      m.chave as modalidade_chave,
      c.numero as concurso,
      c.data_sorteio,
      b.data_limite,
      l.nome as loterica,
      l.cnpj as loterica_cnpj,
      b.qtd_cotas,
      coalesce((select sum(p.qtd_cotas) from mod_sobolao.tbl_sbl_participacoes p where p.id_bolao = b.cod_bolao and p.status in ('reservado','pago','confirmado','premiado')), 0) as cotas_reservadas,
      b.valor_aposta_cota_centavos,
      b.taxa_gestao_percentual,
      (b.valor_aposta_cota_centavos + round(b.valor_aposta_cota_centavos * b.taxa_gestao_percentual / 100.0)::integer) as valor_total_cota_centavos,
      b.status,
      b.numeros_json as numeros,
      b.comprovante_url
    from mod_sobolao.tbl_sbl_boloes b
    join mod_sobolao.tbl_sbl_concursos c on c.cod_concurso = b.id_concurso
    join mod_sobolao.tbl_sbl_modalidades m on m.cod_modalidade = c.id_modalidade
    left join mod_sobolao.tbl_sbl_lotericas l on l.cod_loterica = b.id_loterica
    where b.status in ('aberto','fechando','registrado')
    order by b.data_limite
    limit 40
  ) s;

  select coalesce(jsonb_agg(to_jsonb(s) order by s.atualizado_em desc), '[]'::jsonb)
    into v_minhas
  from (
    select
      p.cod_participacao,
      b.titulo as bolao,
      m.nome as modalidade,
      c.numero as concurso,
      c.data_sorteio,
      p.qtd_cotas as cotas,
      p.valor_total_centavos,
      p.status,
      p.documento_url,
      p.atualizado_em
    from mod_sobolao.tbl_sbl_participacoes p
    join mod_sobolao.tbl_sbl_boloes b on b.cod_bolao = p.id_bolao
    join mod_sobolao.tbl_sbl_concursos c on c.cod_concurso = b.id_concurso
    join mod_sobolao.tbl_sbl_modalidades m on m.cod_modalidade = c.id_modalidade
    where p.id_usuario = v_id_usuario
    order by p.atualizado_em desc
    limit 80
  ) s;

  select coalesce(jsonb_agg(to_jsonb(s) order by s.data_sorteio desc), '[]'::jsonb)
    into v_resultados
  from (
    select
      m.nome as modalidade,
      c.numero as concurso,
      c.data_sorteio,
      c.resultado_json as numeros,
      c.status,
      c.premio_estimado_centavos
    from mod_sobolao.tbl_sbl_concursos c
    join mod_sobolao.tbl_sbl_modalidades m on m.cod_modalidade = c.id_modalidade
    where c.status = 'apurado'
    order by c.data_sorteio desc
    limit 20
  ) s;

  select coalesce(jsonb_agg(to_jsonb(s) order by s.criado_em desc), '[]'::jsonb)
    into v_premios
  from (
    select
      b.titulo as bolao,
      pr.faixa,
      pr.valor_total_centavos as total_centavos,
      r.valor_centavos as minha_parte_centavos,
      r.status,
      r.criado_em
    from mod_sobolao.tbl_sbl_rateios r
    join mod_sobolao.tbl_sbl_premios pr on pr.cod_premio = r.id_premio
    join mod_sobolao.tbl_sbl_boloes b on b.cod_bolao = pr.id_bolao
    where r.id_usuario = v_id_usuario
    order by r.criado_em desc
    limit 80
  ) s;

  select coalesce(jsonb_agg(to_jsonb(s) order by s.atualizado_em desc), '[]'::jsonb)
    into v_chamados
  from (
    select cod_chamado, assunto, prioridade, status, atualizado_em
    from mod_sobolao.tbl_sbl_chamados
    where id_usuario = v_id_usuario
    order by atualizado_em desc
    limit 30
  ) s;

  return jsonb_build_object(
    'ok', true,
    'resumo', v_resumo,
    'fluxo', jsonb_build_array(
      jsonb_build_object('titulo','Formacao','texto','Bolao aberto com cotas, modalidade, concurso e limite definidos.'),
      jsonb_build_object('titulo','Reserva','texto','Participante registra cota e pagamento fica rastreavel.'),
      jsonb_build_object('titulo','Repasse','texto','Valor oficial segue para a loterica parceira; taxa fica separada.'),
      jsonb_build_object('titulo','Documento','texto','Comprovante da aposta e auditoria ficam vinculados ao bolao.'),
      jsonb_build_object('titulo','Apuracao','texto','Resultado, premio e rateio proporcional sao registrados.')
    ),
    'modalidades', (select coalesce(jsonb_agg(jsonb_build_object('chave', chave, 'nome', nome) order by nome), '[]'::jsonb) from mod_sobolao.tbl_sbl_modalidades where ativo),
    'boloes', v_boloes,
    'minhas_participacoes', v_minhas,
    'resultados', v_resultados,
    'premios', v_premios,
    'chamados', v_chamados
  );
end;
$$;

create or replace function public.adm_sobolao_painel_rpc(p_periodo text default null)
returns jsonb
language plpgsql
security definer
set search_path to 'mod_sobolao', 'public', 'extensions', 'auth', 'pg_temp'
as $$
declare
  v_admin boolean := mod_sobolao.fn_sbl_admin_ok('acessar');
  v_base jsonb;
  v_lotericas jsonb;
  v_fila_loterica jsonb;
  v_chamados jsonb;
  v_fila_admin jsonb;
begin
  if not v_admin then
    return jsonb_build_object('ok', false, 'error', 'sem_permissao_sobolao');
  end if;

  select jsonb_build_object(
    'ok', true,
    'resumo', jsonb_build_object(
      'boloes_ativos', (select count(*) from mod_sobolao.tbl_sbl_boloes b where b.status in ('aberto','fechando','registrado')),
      'cotas_reservadas', coalesce((select sum(p.qtd_cotas) from mod_sobolao.tbl_sbl_participacoes p where p.status in ('reservado','pago','confirmado','premiado')), 0),
      'premios_centavos', coalesce((select sum(pr.valor_total_centavos) from mod_sobolao.tbl_sbl_premios pr where pr.status in ('confirmado','pago')), 0),
