create or replace function mod_sobolao.trg_sbl_atualizado()
returns trigger
language plpgsql
as $$
begin
  new.atualizado_em := now();
  return new;
end;
$$;

do $$
declare
  r record;
begin
  for r in
    select unnest(array[
      'tbl_sbl_modalidades',
      'tbl_sbl_lotericas',
      'tbl_sbl_loterica_usuarios',
      'tbl_sbl_concursos',
      'tbl_sbl_boloes',
      'tbl_sbl_participacoes',
      'tbl_sbl_premios',
      'tbl_sbl_rateios',
      'tbl_sbl_chamados'
    ]) as tabela
  loop
    execute format('drop trigger if exists trg_%s_atualizado on mod_sobolao.%I', r.tabela, r.tabela);
    execute format('create trigger trg_%s_atualizado before update on mod_sobolao.%I for each row execute function mod_sobolao.trg_sbl_atualizado()', r.tabela, r.tabela);
  end loop;
end $$;

