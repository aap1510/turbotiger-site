create table if not exists mod_sobolao.tbl_sbl_chamados (
  cod_chamado bigint generated by default as identity primary key,
  uid_origem uuid references auth.users(id) on delete set null,
  id_usuario bigint references public.tbl_usuarios(cod_usuario) on delete set null,
  id_loterica bigint references mod_sobolao.tbl_sbl_lotericas(cod_loterica) on delete set null,
  id_bolao bigint references mod_sobolao.tbl_sbl_boloes(cod_bolao) on delete set null,
