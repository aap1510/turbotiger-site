  assunto text not null,
  mensagem text not null,
  prioridade text not null default 'media',
  status text not null default 'aberto',
  mensagens_json jsonb not null default '[]'::jsonb,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  encerrado_em timestamptz,
  constraint ck_sbl_chamados_status check (status in ('aberto','em_atendimento','respondido','encerrado','cancelado')),
  constraint ck_sbl_chamados_prioridade check (prioridade in ('baixa','media','alta','critica'))
