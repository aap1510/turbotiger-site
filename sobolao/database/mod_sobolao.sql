  end if;
  if nullif(btrim(coalesce(p_titulo, '')), '') is null or coalesce(p_concurso_numero, 0) <= 0 or p_data_sorteio is null or coalesce(p_qtd_cotas, 0) <= 0 or coalesce(p_valor_aposta_cota_centavos, 0) <= 0 or p_data_limite is null then
    return jsonb_build_object('ok', false, 'error', 'dados_obrigatorios');
  end if;

  insert into mod_sobolao.tbl_sbl_modalidades (chave, nome, qtd_numeros, minimo_numero, maximo_numero)
  values (v_chave, initcap(replace(v_chave, '_', ' ')), 6, 1, 60)
  on conflict (chave) do update set ativo = true, atualizado_em = now()
  returning cod_modalidade into v_modalidade;

  insert into mod_sobolao.tbl_sbl_concursos (id_modalidade, numero, data_sorteio, status)
  values (v_modalidade, p_concurso_numero, p_data_sorteio, 'programado')
  on conflict (id_modalidade, numero) do update
  set data_sorteio = excluded.data_sorteio,
      atualizado_em = now()
  returning cod_concurso into v_concurso;

  if v_cnpj is not null then
    select l.cod_loterica into v_loterica
    from mod_sobolao.tbl_sbl_lotericas l
    where l.cnpj = v_cnpj
    limit 1;
  end if;

  if p_cod_bolao is null then
    insert into mod_sobolao.tbl_sbl_boloes (
      id_concurso,
      id_loterica,
      titulo,
      status,
      qtd_cotas,
      valor_aposta_cota_centavos,
      taxa_gestao_percentual,
      data_limite,
      numeros_json,
      criado_por
    )
    values (
      v_concurso,
      v_loterica,
      btrim(p_titulo),
      coalesce(nullif(p_status, ''), 'rascunho'),
      p_qtd_cotas,
      p_valor_aposta_cota_centavos,
      coalesce(p_taxa_gestao_percentual, 10),
      p_data_limite,
      coalesce(p_numeros_json, '[]'::jsonb),
      v_uid
    )
    returning cod_bolao into v_bolao;
  else
    update mod_sobolao.tbl_sbl_boloes
       set id_concurso = v_concurso,
           id_loterica = v_loterica,
           titulo = btrim(p_titulo),
           status = coalesce(nullif(p_status, ''), status),
           qtd_cotas = p_qtd_cotas,
           valor_aposta_cota_centavos = p_valor_aposta_cota_centavos,
           taxa_gestao_percentual = coalesce(p_taxa_gestao_percentual, taxa_gestao_percentual),
           data_limite = p_data_limite,
           numeros_json = coalesce(p_numeros_json, '[]'::jsonb),
           atualizado_em = now()
     where cod_bolao = p_cod_bolao
     returning cod_bolao into v_bolao;

    if v_bolao is null then
      return jsonb_build_object('ok', false, 'error', 'bolao_nao_encontrado');
    end if;
  end if;

  insert into mod_sobolao.tbl_sbl_auditoria(uid_usuario, area, acao, alvo_tipo, alvo_id, detalhes_json)
  values (v_uid, 'sobolao', 'salvar_bolao', 'bolao', v_bolao::text, jsonb_build_object('status', p_status, 'concurso', p_concurso_numero));

  return jsonb_build_object('ok', true, 'cod_bolao', v_bolao);
end;
$$;

create or replace function public.adm_sobolao_status_rpc(
  p_tipo text,
  p_id bigint,
  p_status text,
  p_observacao text default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'mod_sobolao', 'public', 'extensions', 'auth', 'pg_temp'
as $$
declare
  v_uid uuid := auth.uid();
begin
  if not mod_sobolao.fn_sbl_admin_ok('acessar') then
    return jsonb_build_object('ok', false, 'error', 'sem_permissao_sobolao');
  end if;

  if p_tipo = 'bolao' then
    update mod_sobolao.tbl_sbl_boloes set status = p_status, atualizado_em = now() where cod_bolao = p_id;
  elsif p_tipo = 'loterica' then
    update mod_sobolao.tbl_sbl_lotericas set status = p_status, atualizado_em = now() where cod_loterica = p_id;
  elsif p_tipo = 'participacao' then
    update mod_sobolao.tbl_sbl_participacoes set status = p_status, atualizado_em = now() where cod_participacao = p_id;
  elsif p_tipo = 'chamado' then
    update mod_sobolao.tbl_sbl_chamados set status = p_status, atualizado_em = now(), encerrado_em = case when p_status in ('encerrado','cancelado') then now() else encerrado_em end where cod_chamado = p_id;
  end if;

  insert into mod_sobolao.tbl_sbl_auditoria(uid_usuario, area, acao, alvo_tipo, alvo_id, detalhes_json)
  values (v_uid, 'sobolao', 'atualizar_status', p_tipo, p_id::text, jsonb_build_object('status', p_status, 'observacao', p_observacao));

  return jsonb_build_object('ok', true);
end;
$$;

create or replace function public.sobolao_suporte_chamado_rpc(
  p_assunto text,
  p_mensagem text,
  p_origem text default 'usuario',
  p_id_bolao bigint default null,
  p_id_loterica bigint default null
)
returns jsonb
language plpgsql
security definer
set search_path to 'mod_sobolao', 'public', 'extensions', 'auth', 'pg_temp'
as $$
declare
  v_uid uuid := auth.uid();
  v_id_usuario bigint;
  v_chamado bigint;
begin
  if v_uid is null then
    return jsonb_build_object('ok', false, 'error', 'nao_autenticado');
  end if;
  if nullif(btrim(coalesce(p_assunto, '')), '') is null or nullif(btrim(coalesce(p_mensagem, '')), '') is null then
    return jsonb_build_object('ok', false, 'error', 'dados_obrigatorios');
  end if;

  v_id_usuario := mod_sobolao.fn_sbl_usuario_atual();

  insert into mod_sobolao.tbl_sbl_chamados (
    uid_origem,
    id_usuario,
    id_loterica,
    id_bolao,
    origem,
    assunto,
    mensagem,
    mensagens_json
  )
  values (
    v_uid,
    v_id_usuario,
    p_id_loterica,
    p_id_bolao,
    coalesce(nullif(p_origem, ''), 'usuario'),
    btrim(p_assunto),
    btrim(p_mensagem),
    jsonb_build_array(jsonb_build_object('autor', coalesce(nullif(p_origem, ''), 'usuario'), 'mensagem', btrim(p_mensagem), 'criado_em', now()))
  )
  returning cod_chamado into v_chamado;

  return jsonb_build_object('ok', true, 'cod_chamado', v_chamado);
end;
$$;

