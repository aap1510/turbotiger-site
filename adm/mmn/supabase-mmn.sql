-- Turbo Tiger MMN
-- Schema isolado para o modulo de marketing multinivel.
-- Este arquivo e local. Aplique manualmente quando quiser instalar o backend MMN.

begin;

create schema if not exists mod_mmn;

create table if not exists mod_mmn.tbl_mmn_percentuais_nivel (
  cod_mmn_percentual bigint generated by default as identity primary key,
  nivel smallint not null unique check (nivel between 1 and 6),
  percentual numeric(6,3) not null check (percentual >= 0 and percentual <= 33),
  ativo boolean not null default true,
  ordem integer not null default 100,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

create table if not exists mod_mmn.tbl_mmn_ranks (
  cod_mmn_rank bigint generated by default as identity primary key,
  chave text not null unique,
  nome text not null,
  min_ativos_rede integer not null default 0 check (min_ativos_rede >= 0),
  percentual_pool numeric(6,3) not null default 0 check (percentual_pool >= 0 and percentual_pool <= 33),
  ordem integer not null default 100,
  ativo boolean not null default true,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

create table if not exists mod_mmn.tbl_mmn_periodos (
  cod_mmn_periodo bigint generated by default as identity primary key,
  periodo date not null unique,
  status text not null default 'aberto'
    check (status in ('aberto', 'apurando', 'fechado', 'pago', 'cancelado')),
  ticket_centavos integer not null default 1990 check (ticket_centavos >= 0),
  payout_teto_percentual numeric(6,3) not null default 33 check (payout_teto_percentual >= 0 and payout_teto_percentual <= 33),
  usuarios_ativos integer not null default 0 check (usuarios_ativos >= 0),
  mrr_centavos bigint not null default 0 check (mrr_centavos >= 0),
  comissao_centavos bigint not null default 0 check (comissao_centavos >= 0),
  bonus_centavos bigint not null default 0 check (bonus_centavos >= 0),
  observacao text,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  fechado_em timestamptz
);

create table if not exists mod_mmn.tbl_mmn_usuario_status (
  id_usuario bigint primary key references public.tbl_usuarios(cod_usuario) on delete cascade,
  status text not null default 'ativo'
    check (status in ('ativo', 'pendente', 'bloqueado', 'cancelado')),
  rank_chave text references mod_mmn.tbl_mmn_ranks(chave),
  bloqueado_motivo text,
  suporte_observacao text,
  atualizado_por uuid,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

create table if not exists mod_mmn.tbl_mmn_qualificacoes (
  cod_mmn_qualificacao bigint generated by default as identity primary key,
  id_periodo bigint not null references mod_mmn.tbl_mmn_periodos(cod_mmn_periodo) on delete cascade,
  id_usuario bigint not null references public.tbl_usuarios(cod_usuario) on delete cascade,
  diretos_ativos integer not null default 0 check (diretos_ativos >= 0),
  diretos_com_10_ativos integer not null default 0 check (diretos_com_10_ativos >= 0),
  rede_ativos integer not null default 0 check (rede_ativos >= 0),
  qualificado_comissao boolean not null default false,
  qualificado_nivel_5 boolean not null default false,
  qualificado_nivel_6 boolean not null default false,
  rank_chave text references mod_mmn.tbl_mmn_ranks(chave),
  detalhes_json jsonb not null default '{}'::jsonb,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  unique (id_periodo, id_usuario)
);

create table if not exists mod_mmn.tbl_mmn_lancamentos (
  cod_mmn_lancamento bigint generated by default as identity primary key,
  id_periodo bigint not null references mod_mmn.tbl_mmn_periodos(cod_mmn_periodo) on delete cascade,
  id_usuario_beneficiario bigint not null references public.tbl_usuarios(cod_usuario) on delete cascade,
  id_usuario_origem bigint references public.tbl_usuarios(cod_usuario) on delete set null,
  nivel smallint check (nivel between 1 and 6),
  tipo text not null default 'comissao'
    check (tipo in ('comissao', 'bonus_lideranca', 'premiacao', 'ajuste', 'estorno')),
  status text not null default 'pendente'
    check (status in ('pendente', 'confirmado', 'retido', 'pago', 'cancelado')),
  base_centavos bigint not null default 0 check (base_centavos >= 0),
  percentual numeric(6,3) not null default 0 check (percentual >= 0 and percentual <= 33),
  valor_centavos bigint not null default 0 check (valor_centavos >= 0),
  origem_ref text,
  detalhes_json jsonb not null default '{}'::jsonb,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

create table if not exists mod_mmn.tbl_mmn_premios (
  cod_mmn_premio bigint generated by default as identity primary key,
  chave text not null unique,
  nome text not null,
  descricao text,
  rank_chave text references mod_mmn.tbl_mmn_ranks(chave),
  valor_centavos bigint check (valor_centavos is null or valor_centavos >= 0),
  percentual_pool numeric(6,3) check (percentual_pool is null or (percentual_pool >= 0 and percentual_pool <= 33)),
  ativo boolean not null default true,
  ordem integer not null default 100,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now()
);

create table if not exists mod_mmn.tbl_mmn_suporte_ocorrencias (
  cod_mmn_ocorrencia bigint generated by default as identity primary key,
  id_usuario bigint not null references public.tbl_usuarios(cod_usuario) on delete cascade,
  tipo text not null default 'duvida',
  status text not null default 'aberta'
    check (status in ('aberta', 'em_atendimento', 'resolvida', 'cancelada')),
  prioridade text not null default 'normal'
    check (prioridade in ('baixa', 'normal', 'alta', 'critica')),
  assunto text not null,
  detalhe text,
  criado_por uuid,
  atendido_por uuid,
  criado_em timestamptz not null default now(),
  atualizado_em timestamptz not null default now(),
  resolvido_em timestamptz
);

create table if not exists mod_mmn.tbl_mmn_auditoria (
  cod_mmn_auditoria bigint generated by default as identity primary key,
  id_usuario bigint references public.tbl_usuarios(cod_usuario) on delete set null,
  uid_admin uuid,
  area text not null default 'mmn',
  acao text not null,
  alvo_tipo text,
  alvo_id text,
  detalhes_json jsonb not null default '{}'::jsonb,
  criado_em timestamptz not null default now()
);

create index if not exists ix_mmn_lancamentos_beneficiario_periodo
  on mod_mmn.tbl_mmn_lancamentos(id_usuario_beneficiario, id_periodo, status);

create index if not exists ix_mmn_lancamentos_origem
  on mod_mmn.tbl_mmn_lancamentos(id_usuario_origem, nivel);

create unique index if not exists ux_mmn_lancamentos_unico
  on mod_mmn.tbl_mmn_lancamentos(
    id_periodo,
    id_usuario_beneficiario,
    (coalesce(id_usuario_origem, 0)),
    (coalesce(nivel, 0)),
    tipo,
