    return false;
  end if;

  return coalesce(public.adm_usuario_tem_area_uid_rpc(auth.uid(), 'mmn', coalesce(p_acao, 'acessar')), false)
      or coalesce(public.adm_usuario_tem_area_uid_rpc(auth.uid(), 'admin', 'acessar'), false);
end;
$$;

