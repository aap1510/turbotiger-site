create or replace function mod_mmn.fn_usuario_ativo(p_usuario public.tbl_usuarios)
returns boolean
language sql
stable
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
  select
    p_usuario.cod_usuario is not null
    and coalesce((select s.status from mod_mmn.tbl_mmn_usuario_status s where s.id_usuario = p_usuario.cod_usuario), 'ativo') = 'ativo'
    and (
      public.turbotiger_normalizar_licenca(coalesce(p_usuario.licenca, 'trial')) in ('0', 'free')
      or p_usuario.validade is null
      or p_usuario.validade >= current_date
    );
$$;

create or replace function mod_mmn.fn_linha_acima(p_cod_usuario bigint, p_limite integer default 6)
returns table (
  nivel integer,
  cod_usuario bigint,
  login text,
  nome text,
  rank text
)
language sql
stable
security definer
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
  with recursive acima as (
    select
      1 as nivel,
      ind.cod_usuario,
      ind.loginuser,
      btrim(concat_ws(' ', ind.nomeuser, ind.sobrenome)) as nome,
      ind.id_indicador,
      array[p_cod_usuario, ind.cod_usuario]::bigint[] as caminho
    from public.tbl_usuarios u
    join public.tbl_usuarios ind on ind.cod_usuario = u.id_indicador
    where u.cod_usuario = p_cod_usuario
      and ind.cod_usuario <> u.cod_usuario

    union all

    select
      a.nivel + 1,
      ind.cod_usuario,
      ind.loginuser,
      btrim(concat_ws(' ', ind.nomeuser, ind.sobrenome)) as nome,
      ind.id_indicador,
      a.caminho || ind.cod_usuario
    from acima a
    join public.tbl_usuarios atual on atual.cod_usuario = a.cod_usuario
    join public.tbl_usuarios ind on ind.cod_usuario = atual.id_indicador
    where a.nivel < least(greatest(coalesce(p_limite, 6), 1), 6)
      and ind.cod_usuario <> all(a.caminho)
  ),
  ultimo_rank as (
    select distinct on (q.id_usuario)
      q.id_usuario,
      q.rank_chave
    from mod_mmn.tbl_mmn_qualificacoes q
    join mod_mmn.tbl_mmn_periodos p on p.cod_mmn_periodo = q.id_periodo
    order by q.id_usuario, p.periodo desc
  )
  select
    a.nivel,
    a.cod_usuario,
    a.loginuser,
    a.nome,
    coalesce(r.nome, 'Base') as rank
  from acima a
  left join ultimo_rank ur on ur.id_usuario = a.cod_usuario
  left join mod_mmn.tbl_mmn_ranks r on r.chave = ur.rank_chave
  order by a.nivel
  limit least(greatest(coalesce(p_limite, 6), 1), 6);
$$;

create or replace function mod_mmn.fn_linha_abaixo(p_cod_usuario bigint, p_limite integer default 6)
returns table (
  nivel integer,
  cod_usuario bigint,
  login text,
  nome text,
  rank text,
  ativos integer
)
language sql
stable
security definer
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
  with recursive abaixo as (
    select
      1 as nivel,
      u.cod_usuario,
      u.loginuser,
      btrim(concat_ws(' ', u.nomeuser, u.sobrenome)) as nome,
      u.criado_em,
      array[p_cod_usuario, u.cod_usuario]::bigint[] as caminho
    from public.tbl_usuarios u
    where u.id_indicador = p_cod_usuario
      and u.cod_usuario <> p_cod_usuario

    union all

    select
      a.nivel + 1,
      u.cod_usuario,
      u.loginuser,
      btrim(concat_ws(' ', u.nomeuser, u.sobrenome)) as nome,
      u.criado_em,
      a.caminho || u.cod_usuario
    from abaixo a
    join public.tbl_usuarios u on u.id_indicador = a.cod_usuario
    where a.nivel < least(greatest(coalesce(p_limite, 6), 1), 6)
      and u.cod_usuario <> all(a.caminho)
  ),
  resumido as (
    select
      a.*,
      row_number() over (partition by a.nivel order by a.criado_em, a.cod_usuario) as rn
    from abaixo a
  ),
  ultimo_rank as (
    select distinct on (q.id_usuario)
      q.id_usuario,
      q.rank_chave,
      q.rede_ativos
    from mod_mmn.tbl_mmn_qualificacoes q
    join mod_mmn.tbl_mmn_periodos p on p.cod_mmn_periodo = q.id_periodo
    order by q.id_usuario, p.periodo desc
  )
  select
    r.nivel,
    r.cod_usuario,
    r.loginuser,
    r.nome,
    coalesce(rank.nome, 'Base') as rank,
    coalesce(ur.rede_ativos, 0) as ativos
  from resumido r
  left join ultimo_rank ur on ur.id_usuario = r.cod_usuario
  left join mod_mmn.tbl_mmn_ranks rank on rank.chave = ur.rank_chave
  where r.rn = 1
  order by r.nivel
  limit least(greatest(coalesce(p_limite, 6), 1), 6);
$$;

create or replace function mod_mmn.fn_rede_ativos_usuario(p_cod_usuario bigint)
returns integer
language sql
stable
security definer
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
  with recursive rede as (
    select
      u.cod_usuario,
      array[p_cod_usuario, u.cod_usuario]::bigint[] as caminho
    from public.tbl_usuarios u
    where u.id_indicador = p_cod_usuario
      and u.cod_usuario <> p_cod_usuario

    union all

    select
      u.cod_usuario,
      r.caminho || u.cod_usuario
    from rede r
    join public.tbl_usuarios u on u.id_indicador = r.cod_usuario
    where array_length(r.caminho, 1) < 512
      and u.cod_usuario <> all(r.caminho)
  )
  select count(*)::integer
  from rede r
  join public.tbl_usuarios u on u.cod_usuario = r.cod_usuario
  where mod_mmn.fn_usuario_ativo(u);
$$;

create or replace function mod_mmn.fn_diretos_usuario(p_cod_usuario bigint)
returns jsonb
language sql
stable
security definer
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
  with ultimo_rank as (
    select distinct on (q.id_usuario)
      q.id_usuario,
      q.rank_chave,
      q.rede_ativos
    from mod_mmn.tbl_mmn_qualificacoes q
    join mod_mmn.tbl_mmn_periodos p on p.cod_mmn_periodo = q.id_periodo
    order by q.id_usuario, p.periodo desc
  )
  select coalesce(jsonb_agg(
    jsonb_build_object(
      'cod_usuario', u.cod_usuario,
      'login', u.loginuser,
      'nome', btrim(concat_ws(' ', u.nomeuser, u.sobrenome)),
      'rank', coalesce(r.nome, 'Base'),
      'redeAtivos', coalesce(ur.rede_ativos, 0),
      'status', coalesce(s.status, 'ativo')
    )
    order by u.criado_em desc
  ), '[]'::jsonb)
  from public.tbl_usuarios u
  left join mod_mmn.tbl_mmn_usuario_status s on s.id_usuario = u.cod_usuario
  left join ultimo_rank ur on ur.id_usuario = u.cod_usuario
  left join mod_mmn.tbl_mmn_ranks r on r.chave = ur.rank_chave
  where u.id_indicador = p_cod_usuario
    and u.cod_usuario <> p_cod_usuario;
$$;

create or replace function mod_mmn.fn_rede_usuario_json(p_cod_usuario bigint)
returns jsonb
language plpgsql
stable
security definer
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
declare
  v_usuario public.tbl_usuarios%rowtype;
  v_ref text;
  v_qual record;
  v_diretos_ativos integer := 0;
  v_rede_ativos integer := 0;
  v_acima jsonb := '[]'::jsonb;
  v_abaixo jsonb := '[]'::jsonb;
begin
  select *
    into v_usuario
  from public.tbl_usuarios
  where cod_usuario = p_cod_usuario
  limit 1;

  if not found then
    return '{}'::jsonb;
  end if;

  select c.ref
    into v_ref
  from public.tbl_convites_indicacao c
  where c.id_indicador = p_cod_usuario
    and c.ativo
    and (c.expira_em is null or c.expira_em > now())
  order by c.criado_em desc
  limit 1;

  select q.*, r.nome as rank_nome
    into v_qual
  from mod_mmn.tbl_mmn_qualificacoes q
  join mod_mmn.tbl_mmn_periodos p on p.cod_mmn_periodo = q.id_periodo
  left join mod_mmn.tbl_mmn_ranks r on r.chave = q.rank_chave
  where q.id_usuario = p_cod_usuario
  order by p.periodo desc
  limit 1;

  select count(*)::integer
    into v_diretos_ativos
  from public.tbl_usuarios d
  where d.id_indicador = p_cod_usuario
    and d.cod_usuario <> p_cod_usuario
    and mod_mmn.fn_usuario_ativo(d);

  v_rede_ativos := mod_mmn.fn_rede_ativos_usuario(p_cod_usuario);

  select coalesce(jsonb_agg(to_jsonb(x) order by x.nivel), '[]'::jsonb)
    into v_acima
  from mod_mmn.fn_linha_acima(p_cod_usuario, 6) x;

  select coalesce(jsonb_agg(to_jsonb(x) order by x.nivel), '[]'::jsonb)
    into v_abaixo
  from mod_mmn.fn_linha_abaixo(p_cod_usuario, 6) x;

  return jsonb_build_object(
    'usuario', jsonb_build_object(
      'cod_usuario', v_usuario.cod_usuario,
      'login', v_usuario.loginuser,
