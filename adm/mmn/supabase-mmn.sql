create or replace function public.mmn_usuario_painel_rpc()
returns jsonb
language plpgsql
security definer
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
declare
  v_cod_usuario bigint := mod_mmn.fn_usuario_auth();
  v_periodo date := mod_mmn.fn_periodo_mes(current_date);
  v_periodo_id bigint;
  v_ticket numeric := 19.90;
  v_rede jsonb := '{}'::jsonb;
  v_comissoes jsonb := '[]'::jsonb;
  v_premios jsonb := '[]'::jsonb;
  v_usuarios jsonb := '[]'::jsonb;
  v_confirmado numeric := 0;
  v_pendente numeric := 0;
  v_retido numeric := 0;
  v_rede_ativos integer := 0;
begin
  if v_cod_usuario is null then
    return jsonb_build_object('ok', false, 'error', 'usuario_nao_encontrado');
  end if;

  select cod_mmn_periodo
    into v_periodo_id
  from mod_mmn.tbl_mmn_periodos
  where periodo = v_periodo
  limit 1;

  v_rede := mod_mmn.fn_rede_usuario_json(v_cod_usuario);
  v_rede_ativos := coalesce(nullif(v_rede->'usuario'->>'redeAtivos', '')::integer, 0);

  if v_periodo_id is not null then
    select
      coalesce(sum(valor_centavos) filter (where status in ('confirmado', 'pago')), 0)::numeric / 100,
      coalesce(sum(valor_centavos) filter (where status = 'pendente'), 0)::numeric / 100,
      coalesce(sum(valor_centavos) filter (where status = 'retido'), 0)::numeric / 100
      into v_confirmado, v_pendente, v_retido
    from mod_mmn.tbl_mmn_lancamentos
    where id_periodo = v_periodo_id
      and id_usuario_beneficiario = v_cod_usuario;

    select coalesce(jsonb_agg(item order by ordem desc), '[]'::jsonb)
      into v_comissoes
    from (
      select
        l.criado_em as ordem,
        jsonb_build_object(
          'periodo', to_char(v_periodo, 'YYYY-MM'),
          'beneficiario', ub.loginuser,
          'origem', coalesce(uo.loginuser, ''),
          'nivel', l.nivel,
          'base', l.base_centavos::numeric / 100,
          'percentual', l.percentual,
          'valor', l.valor_centavos::numeric / 100,
          'status', l.status
        ) as item
      from mod_mmn.tbl_mmn_lancamentos l
      join public.tbl_usuarios ub on ub.cod_usuario = l.id_usuario_beneficiario
      left join public.tbl_usuarios uo on uo.cod_usuario = l.id_usuario_origem
      where l.id_periodo = v_periodo_id
        and l.id_usuario_beneficiario = v_cod_usuario
      order by l.criado_em desc
      limit 80
    ) x;
  end if;

  select coalesce(jsonb_agg(item), '[]'::jsonb)
    into v_premios
  from (
    select jsonb_build_object(
      'titulo', p.nome,
      'valor', coalesce(p.valor_centavos, 0)::numeric / 100,
      'status', 'previsto',
      'descricao', coalesce(p.descricao, '')
    ) as item
    from mod_mmn.tbl_mmn_premios p
    where p.ativo
    order by p.ordem
    limit 4
  ) premios;

  v_usuarios := jsonb_build_array(v_rede->'usuario') || coalesce(v_rede->'diretos', '[]'::jsonb);

  return jsonb_build_object(
    'ok', true,
    'resumo', jsonb_build_object(
      'ativos', greatest(v_rede_ativos, 1),
      'mrr', greatest(v_rede_ativos, 1) * v_ticket,
      'payout', case when greatest(v_rede_ativos, 1) * v_ticket > 0 then round(((v_confirmado + v_pendente + v_retido) / (greatest(v_rede_ativos, 1) * v_ticket)) * 100, 2) else 0 end,
      'comissoes', v_confirmado + v_pendente + v_retido,
      'bonificacoes', 0,
      'pendencias', (select count(*) from mod_mmn.tbl_mmn_lancamentos where id_usuario_beneficiario = v_cod_usuario and status in ('pendente', 'retido')),
      'ticket', v_ticket,
      'payoutCap', 33,
      'niveis', 6,
      'margem', 100 - case when greatest(v_rede_ativos, 1) * v_ticket > 0 then round(((v_confirmado + v_pendente + v_retido) / (greatest(v_rede_ativos, 1) * v_ticket)) * 100, 2) else 0 end
    ),
    'usuarios', v_usuarios,
    'rede', v_rede,
    'comissoes', v_comissoes,
    'premios', v_premios,
    'ranks', (select coalesce(jsonb_agg(jsonb_build_object('rank', nome, 'criterio', min_ativos_rede || ' ativos na rede', 'pool', percentual_pool || '%') order by ordem), '[]'::jsonb) from mod_mmn.tbl_mmn_ranks where ativo),
    'regras', jsonb_build_object(
      'levels', (select coalesce(jsonb_agg(jsonb_build_object('nivel', nivel, 'percentual', percentual) order by ordem), '[]'::jsonb) from mod_mmn.tbl_mmn_percentuais_nivel where ativo),
      'qualificacao', jsonb_build_array(
        jsonb_build_object('titulo', 'Diretos ativos', 'texto', 'Minimo de 3 diretos ativos para habilitar comissao.'),
        jsonb_build_object('titulo', 'Niveis 5 e 6', 'texto', 'Exigem continuidade e diretos com rede ativa validada.'),
        jsonb_build_object('titulo', 'Payout', 'texto', 'Teto operacional de 33% sobre receita confirmada.')
      )
    )
  );
end;
$$;

create or replace function public.adm_mmn_painel_rpc(p_periodo date default null)
returns jsonb
language plpgsql
security definer
set search_path = mod_mmn, public, extensions, auth, net, pg_temp
as $$
declare
  v_periodo date := mod_mmn.fn_periodo_mes(coalesce(p_periodo, current_date));
  v_periodo_id bigint;
  v_ticket numeric := 19.90;
  v_usuarios_ativos integer;
  v_mrr numeric;
  v_comissoes numeric := 0;
  v_bonus numeric := 0;
  v_pendencias integer := 0;
  v_usuarios jsonb := '[]'::jsonb;
  v_rede jsonb := '{}'::jsonb;
begin
  if not mod_mmn.fn_admin_tem_acesso('acessar') then
    return jsonb_build_object('ok', false, 'error', 'sem_permissao_mmn');
  end if;

  select cod_mmn_periodo
    into v_periodo_id
  from mod_mmn.tbl_mmn_periodos
  where periodo = v_periodo
  limit 1;

  select count(*)
    into v_usuarios_ativos
  from public.tbl_usuarios u
  where mod_mmn.fn_usuario_ativo(u);

  v_mrr := coalesce(v_usuarios_ativos, 0) * v_ticket;

  if v_periodo_id is not null then
    select
      coalesce(sum(valor_centavos) filter (where tipo = 'comissao'), 0)::numeric / 100,
      coalesce(sum(valor_centavos) filter (where tipo in ('bonus_lideranca', 'premiacao')), 0)::numeric / 100,
      count(*) filter (where status in ('pendente', 'retido'))
      into v_comissoes, v_bonus, v_pendencias
    from mod_mmn.tbl_mmn_lancamentos
    where id_periodo = v_periodo_id;
  end if;

  select coalesce(jsonb_agg(item order by rede_ativos desc, cod_usuario), '[]'::jsonb)
    into v_usuarios
  from (
    select
      u.cod_usuario,
      coalesce(q.rede_ativos, calc.rede_ativos, 0) as rede_ativos,
      jsonb_build_object(
        'cod_usuario', u.cod_usuario,
        'login', u.loginuser,
        'nome', btrim(concat_ws(' ', u.nomeuser, u.sobrenome)),
        'indicador', coalesce(ind.loginuser, ''),
        'diretos', coalesce(q.diretos_ativos, calc.diretos_ativos, 0),
        'redeAtivos', coalesce(q.rede_ativos, calc.rede_ativos, 0),
        'rank', coalesce(r.nome, 'Base'),
        'status', coalesce(s.status, 'ativo'),
        'ref', coalesce(c.ref, '')
      ) as item
    from public.tbl_usuarios u
    left join public.tbl_usuarios ind on ind.cod_usuario = u.id_indicador and ind.cod_usuario <> u.cod_usuario
    left join mod_mmn.tbl_mmn_usuario_status s on s.id_usuario = u.cod_usuario
    left join lateral (
      select q.*
      from mod_mmn.tbl_mmn_qualificacoes q
      join mod_mmn.tbl_mmn_periodos p on p.cod_mmn_periodo = q.id_periodo
      where q.id_usuario = u.cod_usuario
      order by p.periodo desc
      limit 1
    ) q on true
    left join lateral (
      select
        (select count(*)::integer
         from public.tbl_usuarios d
         where d.id_indicador = u.cod_usuario
           and d.cod_usuario <> u.cod_usuario
           and mod_mmn.fn_usuario_ativo(d)) as diretos_ativos,
        mod_mmn.fn_rede_ativos_usuario(u.cod_usuario) as rede_ativos
    ) calc on true
    left join mod_mmn.tbl_mmn_ranks r on r.chave = q.rank_chave
    left join lateral (
      select c.ref
      from public.tbl_convites_indicacao c
      where c.id_indicador = u.cod_usuario
        and c.ativo
        and (c.expira_em is null or c.expira_em > now())
      order by c.criado_em desc
      limit 1
    ) c on true
    order by coalesce(q.rede_ativos, calc.rede_ativos, 0) desc, u.cod_usuario
    limit 120
  ) x;

  select mod_mmn.fn_rede_usuario_json((v_usuarios->0->>'cod_usuario')::bigint)
    into v_rede
  where jsonb_array_length(v_usuarios) > 0;

  return jsonb_build_object(
    'ok', true,
    'resumo', jsonb_build_object(
      'ativos', v_usuarios_ativos,
      'mrr', v_mrr,
      'payout', case when v_mrr > 0 then round(((v_comissoes + v_bonus) / v_mrr) * 100, 2) else 0 end,
      'comissoes', v_comissoes,
      'bonificacoes', v_bonus,
      'pendencias', v_pendencias,
      'ticket', v_ticket,
      'payoutCap', 33,
      'niveis', 6,
      'margem', case when v_mrr > 0 then round(((v_mrr - v_comissoes - v_bonus) / v_mrr) * 100, 2) else 100 end
    ),
    'usuarios', v_usuarios,
    'rede', coalesce(v_rede, '{}'::jsonb),
    'comissoes', (
      select coalesce(jsonb_agg(item order by ordem desc), '[]'::jsonb)
      from (
        select
          l.criado_em as ordem,
          jsonb_build_object(
            'periodo', to_char(v_periodo, 'YYYY-MM'),
            'beneficiario', ub.loginuser,
            'origem', coalesce(uo.loginuser, ''),
            'nivel', l.nivel,
            'base', l.base_centavos::numeric / 100,
            'percentual', l.percentual,
            'valor', l.valor_centavos::numeric / 100,
            'status', l.status
          ) as item
        from mod_mmn.tbl_mmn_lancamentos l
        join public.tbl_usuarios ub on ub.cod_usuario = l.id_usuario_beneficiario
        left join public.tbl_usuarios uo on uo.cod_usuario = l.id_usuario_origem
        where v_periodo_id is not null
          and l.id_periodo = v_periodo_id
        order by l.criado_em desc
        limit 120
      ) lanc
    ),
    'premios', (
      select coalesce(jsonb_agg(jsonb_build_object('titulo', nome, 'valor', coalesce(valor_centavos, 0)::numeric / 100, 'status', 'previsto', 'descricao', coalesce(descricao, '')) order by ordem), '[]'::jsonb)
      from mod_mmn.tbl_mmn_premios
      where ativo
    ),
    'ranks', (
      select coalesce(jsonb_agg(jsonb_build_object('rank', nome, 'criterio', min_ativos_rede || ' ativos na rede', 'pool', percentual_pool || '%') order by ordem), '[]'::jsonb)
      from mod_mmn.tbl_mmn_ranks
      where ativo
    ),
    'regras', jsonb_build_object(
      'levels', (select coalesce(jsonb_agg(jsonb_build_object('nivel', nivel, 'percentual', percentual) order by ordem), '[]'::jsonb) from mod_mmn.tbl_mmn_percentuais_nivel where ativo),
      'qualificacao', jsonb_build_array(
        jsonb_build_object('titulo', 'Diretos ativos', 'texto', 'Minimo de 3 diretos ativos para habilitar comissao.'),
        jsonb_build_object('titulo', 'Base ativa', 'texto', 'Calculo considera base liquida ativa, nao historico bruto.'),
        jsonb_build_object('titulo', 'Payout', 'texto', 'Teto operacional de 33% sobre receita confirmada.')
      )
    )
  );
end;
$$;

